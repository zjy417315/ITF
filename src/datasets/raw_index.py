"""
raw-rgb 数据索引模块
===================

管理 raw-rgb 成对数据的索引，所有 Dataset 都从这里获取样本信息。

Classes:
    RawSample: 描述单个样本的元信息
    RawIndex: 管理一组样本，支持划分和访问
"""

import os
import json
import random
from pathlib import Path
from dataclasses import dataclass, field, asdict
from typing import Optional, List, Dict, Tuple, Union, Any
from enum import Enum
import logging
from ..utils import env  # noqa: F401  # 确保环境变量先被设置
logger = logging.getLogger(__name__)


class SplitType(Enum):
    """数据集划分类型"""
    TRAIN = "train"
    VAL = "val"
    TEST = "test"
    ALL = "all"


@dataclass
class RawSample:
    """
    描述单个 raw-rgb 样本的元信息

    Attributes:
        sample_id: 全局唯一标识符
        raw_path: raw 文件的绝对路径
        rgb_path: 对应 rgb 文件的绝对路径 (可选，如果没有则由ISP生成)
        scene_id: 场景编号 (可选，用于按场景划分)
        camera_model: 相机型号 (可选)
        meta: 其他元信息 (曝光时间、ISO、白平衡等)
    """
    sample_id: int
    raw_path: str
    rgb_path: Optional[str] = None
    scene_id: Optional[int] = None
    camera_model: Optional[str] = None
    meta: Dict[str, Any] = field(default_factory=dict)

    def __post_init__(self):
        """验证路径存在性"""
        if not os.path.exists(self.raw_path):
            raise FileNotFoundError(f"raw file not found: {self.raw_path}")
        if self.rgb_path and not os.path.exists(self.rgb_path):
            logger.warning(f"rgb file not found: {self.rgb_path}, will be generated by ISP")
            self.rgb_path = None

    @property
    def raw_filename(self) -> str:
        """获取RAW文件名"""
        return os.path.basename(self.raw_path)

    @property
    def base_name(self) -> str:
        """获取不带扩展名的基础文件名"""
        return os.path.splitext(self.raw_filename)[0]

    @property
    def has_rgb(self) -> bool:
        """是否有对应的RGB文件"""
        return self.rgb_path is not None

    def to_dict(self) -> Dict:
        """转换为字典"""
        return asdict(self)

    @classmethod
    def from_dict(cls, data: Dict) -> "RawSample":
        """从字典创建"""
        return cls(**data)


@dataclass
class SplitConfig:
    """
    数据集划分配置

    Attributes:
        train_ratio: 训练集比例
        val_ratio: 验证集比例
        test_ratio: 测试集比例
        random_seed: 随机种子
        by_scene: 是否按场景划分 (避免同一场景出现在不同集合)
    """
    train_ratio: float = 0.8
    val_ratio: float = 0.1
    test_ratio: float = 0.1
    random_seed: int = 42
    by_scene: bool = False

    def __post_init__(self):
        total = self.train_ratio + self.val_ratio + self.test_ratio
        if abs(total - 1.0) > 1e-6:
            raise ValueError(f"Split ratios must sum to 1.0, got {total}")


class RawIndex:
    """
    raw-rgb 数据索引管理器

    管理一组 RawSample，支持:
    - 按索引访问
    - 训练/验证/测试划分
    - 按相机型号/场景筛选
    - 序列化/反序列化

    Example:
        >>> index = RawIndex.from_folder("./data")
        >>> print(f"Total samples: {len(index)}")
        >>> sample = index.get_sample(0)
        >>> train_idx, val_idx, test_idx = index.train_val_test_split(SplitConfig())
    """

    def __init__(self, samples: Optional[List[RawSample]] = None):
        """
        初始化索引

        Args:
            samples: RawSample 列表
        """
        self._samples: List[RawSample] = samples or []
        self._id_to_idx: Dict[int, int] = {}
        self._rebuild_index()

    def _rebuild_index(self):
        """重建内部索引"""
        self._id_to_idx = {s.sample_id: i for i, s in enumerate(self._samples)}

    def __len__(self) -> int:
        """返回样本总数"""
        return len(self._samples)

    def __getitem__(self, idx: int) -> RawSample:
        """按索引访问样本"""
        return self._samples[idx]

    def __iter__(self):
        """迭代所有样本"""
        return iter(self._samples)

    def get_sample(self, idx: int) -> RawSample:
        """
        按索引获取样本

        Args:
            idx: 整数索引

        Returns:
            对应的 RawSample

        Raises:
            IndexError: 如果索引越界
        """
        if idx < 0 or idx >= len(self._samples):
            raise IndexError(f"Index {idx} out of range [0, {len(self._samples)})")
        return self._samples[idx]

    def get_sample_by_id(self, sample_id: int) -> Optional[RawSample]:
        """
        按 sample_id 获取样本

        Args:
            sample_id: 样本ID

        Returns:
            对应的 RawSample，如果不存在返回 None
        """
        idx = self._id_to_idx.get(sample_id)
        return self._samples[idx] if idx is not None else None

    def add_sample(self, sample: RawSample):
        """添加一个样本"""
        if sample.sample_id in self._id_to_idx:
            raise ValueError(f"Sample ID {sample.sample_id} already exists")
        self._samples.append(sample)
        self._id_to_idx[sample.sample_id] = len(self._samples) - 1

    def filter_by_camera(self, camera_model: str) -> "RawIndex":
        """
        按相机型号筛选

        Args:
            camera_model: 相机型号

        Returns:
            筛选后的新 RawIndex
        """
        filtered = [s for s in self._samples if s.camera_model == camera_model]
        return RawIndex(filtered)

    def filter_by_has_rgb(self, has_rgb: bool = True) -> "RawIndex":
        """
        按是否有RGB筛选

        Args:
            has_rgb: 是否需要有RGB

        Returns:
            筛选后的新 RawIndex
        """
        filtered = [s for s in self._samples if s.has_rgb == has_rgb]
        return RawIndex(filtered)

    def get_camera_models(self) -> List[str]:
        """获取所有相机型号"""
        models = set()
        for s in self._samples:
            if s.camera_model:
                models.add(s.camera_model)
        return sorted(list(models))

    def get_statistics(self) -> Dict[str, Any]:
        """获取数据集统计信息"""
        stats = {
            "total_samples": len(self._samples),
            "with_rgb": sum(1 for s in self._samples if s.has_rgb),
            "without_rgb": sum(1 for s in self._samples if not s.has_rgb),
            "camera_models": {},
            "scenes": len(set(s.scene_id for s in self._samples if s.scene_id is not None))
        }

        # 按相机统计
        for s in self._samples:
            model = s.camera_model or "unknown"
            stats["camera_models"][model] = stats["camera_models"].get(model, 0) + 1

        return stats

    def train_val_test_split(
            self,
            split_cfg: Optional[SplitConfig] = None
    ) -> Tuple["RawIndex", "RawIndex", "RawIndex"]:
        """
        划分训练/验证/测试集

        Args:
            split_cfg: 划分配置，默认使用 80/10/10

        Returns:
            (train_index, val_index, test_index)
        """
        if split_cfg is None:
            split_cfg = SplitConfig()

        # 设置随机种子
        random.seed(split_cfg.random_seed)

        if split_cfg.by_scene:
            # 按场景划分
            return self._split_by_scene(split_cfg)
        else:
            # 随机划分
            return self._split_random(split_cfg)

    def _split_random(
            self,
            cfg: SplitConfig
    ) -> Tuple["RawIndex", "RawIndex", "RawIndex"]:
        """随机划分"""
        indices = list(range(len(self._samples)))
        random.shuffle(indices)

        n = len(indices)
        n_train = int(n * cfg.train_ratio)
        n_val = int(n * cfg.val_ratio)

        train_indices = indices[:n_train]
        val_indices = indices[n_train:n_train + n_val]
        test_indices = indices[n_train + n_val:]

        train_samples = [self._samples[i] for i in train_indices]
        val_samples = [self._samples[i] for i in val_indices]
        test_samples = [self._samples[i] for i in test_indices]

        return RawIndex(train_samples), RawIndex(val_samples), RawIndex(test_samples)

    def _split_by_scene(
            self,
            cfg: SplitConfig
    ) -> Tuple["RawIndex", "RawIndex", "RawIndex"]:
        """按场景划分，确保同一场景的样本在同一个集合"""
        # 按场景分组
        scene_to_samples: Dict[Optional[int], List[RawSample]] = {}
        for s in self._samples:
            scene_id = s.scene_id
            if scene_id not in scene_to_samples:
                scene_to_samples[scene_id] = []
            scene_to_samples[scene_id].append(s)

        # 随机打乱场景
        scenes = list(scene_to_samples.keys())
        random.shuffle(scenes)

        # 按场景数量划分
        n_scenes = len(scenes)
        n_train = int(n_scenes * cfg.train_ratio)
        n_val = int(n_scenes * cfg.val_ratio)

        train_scenes = scenes[:n_train]
        val_scenes = scenes[n_train:n_train + n_val]
        test_scenes = scenes[n_train + n_val:]

        # 收集样本
        train_samples = [s for scene in train_scenes for s in scene_to_samples[scene]]
        val_samples = [s for scene in val_scenes for s in scene_to_samples[scene]]
        test_samples = [s for scene in test_scenes for s in scene_to_samples[scene]]

        return RawIndex(train_samples), RawIndex(val_samples), RawIndex(test_samples)

    def subset(self, indices: List[int]) -> "RawIndex":
        """
        获取子集

        Args:
            indices: 索引列表

        Returns:
            子集的新 RawIndex
        """
        samples = [self._samples[i] for i in indices]
        return RawIndex(samples)

    @classmethod
    def from_folder(
            cls,
            root_dir: str,
            raw_subdir: str = "raw",
            rgb_subdir: str = "rgb",
            raw_extensions: Optional[List[str]] = None,
            rgb_extensions: Optional[List[str]] = None
    ) -> "RawIndex":
        """
        从文件夹构建索引

        自动扫描目录，匹配 raw 和 rgb 文件。
        匹配规则：相同的基础文件名（不含扩展名）

        Args:
            root_dir: 数据根目录
            raw_subdir: raw 文件子目录名
            rgb_subdir: rgb 文件子目录名
            raw_extensions: raw 文件扩展名列表
            rgb_extensions: rgb 文件扩展名列表

        Returns:
            构建好的 RawIndex

        Example:
            data/
                raw/
                    img001.dng
                    img002.nef
                rgb/
                    img001.jpg
                    img002.png
        """
        if raw_extensions is None:
            raw_extensions = [".dng", ".DNG", ".nef", ".NEF", ".cr2", ".CR2",
                              ".arw", ".ARW", ".orf", ".ORF", ".rw2", ".RW2"]
        if rgb_extensions is None:
            rgb_extensions = [".jpg", ".JPG", ".jpeg", ".JPEG", ".png", ".PNG",
                              ".tif", ".TIF", ".tiff", ".TIFF"]

        root_path = Path(root_dir)
        raw_dir = root_path / raw_subdir
        rgb_dir = root_path / rgb_subdir

        if not raw_dir.exists():
            raise FileNotFoundError(f"raw directory not found: {raw_dir}")

        # 扫描 raw 文件
        raw_files: Dict[str, Path] = {}
        for ext in raw_extensions:
            for f in raw_dir.glob(f"*{ext}"):
                base_name = f.stem
                if base_name not in raw_files:
                    raw_files[base_name] = f

        logger.info(f"Found {len(raw_files)} raw files in {raw_dir}")

        # 扫描 rgb 文件
        rgb_files: Dict[str, Path] = {}
        if rgb_dir.exists():
            for ext in rgb_extensions:
                for f in rgb_dir.glob(f"*{ext}"):
                    base_name = f.stem
                    if base_name not in rgb_files:
                        rgb_files[base_name] = f
            logger.info(f"Found {len(rgb_files)} rgb files in {rgb_dir}")

        # 创建样本
        samples = []
        for idx, (base_name, raw_path) in enumerate(sorted(raw_files.items())):
            rgb_path = rgb_files.get(base_name)

            # 尝试从文件名推断相机型号
            camera_model = cls._infer_camera_model(raw_path)

            sample = RawSample(
                sample_id=idx,
                raw_path=str(raw_path.absolute()),
                rgb_path=str(rgb_path.absolute()) if rgb_path else None,
                camera_model=camera_model
            )
            samples.append(sample)

        logger.info(f"Created index with {len(samples)} samples")
        return cls(samples)

    @staticmethod
    def _infer_camera_model(raw_path: Path) -> Optional[str]:
        """从文件名推断相机型号"""
        filename = raw_path.stem.lower()

        # 常见相机型号模式
        camera_patterns = {
            "canon": ["canon", "eos", "5d", "6d", "7d", "80d", "90d"],
            "nikon": ["nikon", "d700", "d800", "d850", "d7000", "d7500"],
            "sony": ["sony", "a7", "a9", "dslr-a"],
            "fuji": ["fuji", "x-pro", "x100", "x-t"],
            "olympus": ["olympus", "e-m", "e-p"],
            "panasonic": ["panasonic", "gh", "gx", "dmc"],
            "pentax": ["pentax", "k-"],
            "leica": ["leica", "m9", "m10", "x1", "x2"],
        }

        for brand, patterns in camera_patterns.items():
            for pattern in patterns:
                if pattern in filename:
                    return brand

        return None

    def save(self, filepath: str):
        """
        保存索引到文件

        Args:
            filepath: JSON 文件路径
        """
        data = {
            "version": "1.0",
            "samples": [s.to_dict() for s in self._samples]
        }
        with open(filepath, 'w', encoding='utf-8') as f:
            json.dump(data, f, indent=2, ensure_ascii=False)
        logger.info(f"Saved index to {filepath}")

    @classmethod
    def load(cls, filepath: str) -> "RawIndex":
        """
        从文件加载索引

        Args:
            filepath: JSON 文件路径

        Returns:
            加载的 RawIndex
        """
        with open(filepath, 'r', encoding='utf-8') as f:
            data = json.load(f)

        samples = [RawSample.from_dict(s) for s in data["samples"]]
        logger.info(f"Loaded index with {len(samples)} samples from {filepath}")
        return cls(samples)

    def __repr__(self) -> str:
        stats = self.get_statistics()
        return (f"RawIndex(samples={stats['total_samples']}, "
                f"with_rgb={stats['with_rgb']}, "
                f"cameras={len(stats['camera_models'])})")


# 便捷函数
def create_index_from_folder(
        root_dir: str,
        save_path: Optional[str] = None
) -> RawIndex:
    """
    便捷函数：从文件夹创建索引

    Args:
        root_dir: 数据根目录
        save_path: 可选，保存索引的路径

    Returns:
        RawIndex 对象
    """
    index = RawIndex.from_folder(root_dir)

    if save_path:
        index.save(save_path)

    return index


